/**
 * Generates src/embed-pieces.js — base64 data URLs for the default piece set.
 * Run: node scripts/build-embed-pieces.js
 */
import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const piecesDir = resolve(__dirname, '../public/pieces');

const PIECES = ['wK', 'wQ', 'wR', 'wB', 'wN', 'wP', 'bK', 'bQ', 'bR', 'bB', 'bN', 'bP'];
const ROLES = { wK: 'king', wQ: 'queen', wR: 'rook', wB: 'bishop', wN: 'knight', wP: 'pawn',
                bK: 'king', bQ: 'queen', bR: 'rook', bB: 'bishop', bN: 'knight', bP: 'pawn' };
const COLORS = { w: 'white', b: 'black' };

const urls = {};
for (const p of PIECES) {
    const data = readFileSync(resolve(piecesDir, `${p}.webp`));
    urls[p] = `data:image/webp;base64,${data.toString('base64')}`;
}

const lines = [
    '// Auto-generated by scripts/build-embed-pieces.js — do not edit',
    `export const PIECE_URLS = ${JSON.stringify(urls)};`,
    '',
    '/** Inject piece images as CSS background-image rules (chessground) + patch icon src attributes */',
    'export function injectPieces() {',
    '    const style = document.createElement("style");',
    '    style.id = "embed-pieces";',
    '    const rules = [];',
    `    for (const [piece, url] of Object.entries(PIECE_URLS)) {`,
    `        const color = piece[0] === "w" ? "white" : "black";`,
    `        const role = ${JSON.stringify(ROLES)}[piece];`,
    '        rules.push(`.cg-wrap piece.${role}.${color} { background-image: url("${url}"); }`);',
    '    }',
    '    style.textContent = rules.join("\\n");',
    '    document.head.appendChild(style);',
    '',
    '    // Patch any <img> elements that reference /pieces/',
    '    for (const img of document.querySelectorAll("img[src*=\'/pieces/\']")) {',
    '        const match = img.src.match(/\\/(\\w+)\\.webp$/);',
    '        if (match && PIECE_URLS[match[1]]) img.src = PIECE_URLS[match[1]];',
    '    }',
    '}',
];

writeFileSync(resolve(__dirname, '../src/embed-pieces.js'), lines.join('\n') + '\n');
console.log(`Generated src/embed-pieces.js (${PIECES.length} pieces)`);
