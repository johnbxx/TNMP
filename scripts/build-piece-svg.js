/**
 * Download cburnett SVG chess pieces from lichess and generate
 * an inline JS module for use in the worker's OG board renderer.
 *
 * Run from project root: node scripts/build-piece-svg.js
 *
 * Pieces are by Colin Burnett, licensed CC BY-SA 3.0.
 * https://github.com/lichess-org/lila/tree/master/public/piece/cburnett
 */

const BASE_URL = 'https://raw.githubusercontent.com/lichess-org/lila/master/public/piece/cburnett';

const PIECES = ['wK', 'wQ', 'wR', 'wB', 'wN', 'wP', 'bK', 'bQ', 'bR', 'bB', 'bN', 'bP'];

const OUT_PATH = new URL('../worker/src/piece-svg.js', import.meta.url).pathname;

async function main() {
    const entries = {};

    for (const piece of PIECES) {
        const url = `${BASE_URL}/${piece}.svg`;
        console.log(`Fetching ${piece}...`);
        const res = await fetch(url);
        if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status}`);
        let svg = await res.text();
        // Trim whitespace
        svg = svg.trim();
        entries[piece] = svg;
    }

    // Build JS module
    const lines = [
        '// Auto-generated by scripts/build-piece-svg.js',
        '// cburnett chess pieces by Colin Burnett — CC BY-SA 3.0',
        '// https://github.com/lichess-org/lila/tree/master/public/piece/cburnett',
        '',
        'export const PIECE_SVG = {',
    ];

    for (const piece of PIECES) {
        // Escape backticks and ${} in SVG content for template literal safety
        const escaped = entries[piece].replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
        lines.push(`    ${piece}: \`${escaped}\`,`);
    }

    lines.push('};');
    lines.push('');

    const { writeFileSync } = await import('node:fs');
    writeFileSync(OUT_PATH, lines.join('\n'), 'utf-8');
    console.log(`\nWrote ${OUT_PATH}`);
    console.log(`${PIECES.length} pieces generated.`);
}

main().catch(err => {
    console.error(err);
    process.exit(1);
});
